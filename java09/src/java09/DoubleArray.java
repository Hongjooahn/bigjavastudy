package java09;

public class DoubleArray {

	public static void main(String[] args) {
		int [][]num = {{1,2,3},
						{4,5,6}};
		
		System.out.println(num[1][2]+""+num);
		for(int i = 0 ; i<num.length; i++) {
			for(int j = 0 ; j<num[j].length ; j++) {
				System.out.println("*");
				
			}
		}
		
	}

}
